package com.example.day04;

public class Exam2 {
    //배열의 모든 요소를 출력하는 코드를 for 루프를 사용하여 작성하세요.
    public static void main(String[] args) {
        double[] doubles = {1.0, 2.5, 3.7, 4.4};

        for(int i = 0; i < doubles.length; i++) {
            System.out.println(doubles[i]);
        }
    }
}
