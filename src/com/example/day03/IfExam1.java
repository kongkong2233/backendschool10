public class IfExam1 {
    public static void main(String[] args) {
        int a = 5;

        if (a > 4) { // if(조건문) {조건문이 참일 경우 실행되는 블록}
            System.out.println("a는 4보다 크다");
        }

        //a가 짝수면 참이다, 의 조건문은?
        //a % 2 = 0 (짝수면 2로 나누었을 때 무조건 0이여야 하기 때문)
    }
}
