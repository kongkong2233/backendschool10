package com.example.day03;

public class IfExam2 {
    public static void main(String[] args) {
        int a = 3;

        if (a > 4) { //if(조건문) {조건문이 참일 경우 실행} else {조건문이 거짓일 경우 실행}
            System.out.println("a는 4보다 크다");
        } else {
            System.out.println("a는 4와 같거나 보다 작다");
        }
    }
}
